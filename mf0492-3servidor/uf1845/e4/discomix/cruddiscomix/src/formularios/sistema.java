    public void eliminarRegistros (){
        
        
        int filaseleccionada=tablamegamixes.getSelectedRow();
        
        try {
            String SQL="delete from megamixes where idmegamixes="+tablamegamixes.getValueAt(filaseleccionada, 0);
            
            Statement st=con.createStatement();
            
            int n=st.executeUpdate(SQL);
            
            if (n>=0) {
                
                JOptionPane.showMessageDialog(null, "Registro Eliminado");
                
                
                
            }
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Error al Eliminar Registros"+ e.getMessage());
            
        }
        
    }
    
    
