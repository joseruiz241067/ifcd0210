        public void limpiarCajas () {
            
            txtAlbum.setText("");
            txtDiscografica.setText("");
            txtValoracion.setText("");
            cbEstilo.setSelectedItem(null);
            cbDiscjockey.setSelectedItem(null);
            
            
            
        
    }//GEN-LAST:event_btnGuardarActionPerformed
public void ActualizarDatos(){
        
        
       
        try {
             
            String SQL=" Update megamixes set album=?,discografica=?,discjockey=?,valoracion=?,estilo=? where idmegamixes=? ";
            
            int filaseleccionada=tablamegamixes.getSelectedRow();
            String dao=(String)tablamegamixes.getValueAt(filaseleccionada, 0);
            
            
            
            PreparedStatement pst=con.prepareStatement (SQL);
            
            pst.setString(1, txtAlbum.getText());
            pst.setString(2, txtDiscografica.getText());
            
            int seleccionado=cbDiscjockey.getSelectedIndex();
            pst.setString(3, cbDiscjockey.getItemAt(seleccionado));
            
            pst.setDouble(4, Double.parseDouble(txtValoracion.getText()));
            
            int seleccionado1=cbEstilo.getSelectedIndex();
            pst.setString(5, cbEstilo.getItemAt(seleccionado1));
            
            pst.setString (6, dao);
            
            pst.execute();
            
            JOptionPane.showMessageDialog(null,"Registro Editado Exitoso");
            
            
            
            
             
            
            
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,"Error de Edición" + e.getMessage());
            
            
        }
     
        
    }
        
        
        
