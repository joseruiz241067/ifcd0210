      public void filtrarDatos(String valor) {
        
        String [] titulos={"ID", "Album", "Discografica", "Discjockey", "Valoracion", "Estilo"};
        String [] registros=new String[7];
        
        DefaultTableModel modelo=new DefaultTableModel(null,titulos);
        
        String SQL="select * from megamixes where album like '%"+valor+"%'"; 
        
        try {
            
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery(SQL);
            
                    
            
            
          while (rs.next())  {
              
           registros[0]=rs.getString("idmegamixes");
           registros[1]=rs.getString("album");
           registros[2]=rs.getString("discografica");
           registros[3]=rs.getString("discjockey");
           registros[4]=rs.getString("valoracion");
           registros[5]=rs.getString("estilo");
           
           modelo.addRow(registros);
           
              
          }
          
          tablamegamixes.setModel(modelo);
          
          
          
          
        }catch (Exception e) {
            
         JOptionPane.showMessageDialog(null, "Error al Mostrar Datos"+  e.getMessage());
         
        }
        
        
        
    }
    
