    private void tablamegamixesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablamegamixesMouseClicked
        
        int filaseleccionada=tablamegamixes.rowAtPoint(evt.getPoint());
        
        txtAlbum.setText(tablamegamixes.getValueAt(filaseleccionada, 1).toString());
        txtDiscografica.setText(tablamegamixes.getValueAt(filaseleccionada, 2).toString());
        cbDiscjockey.setSelectedItem(tablamegamixes.getValueAt(filaseleccionada, 3));
        txtValoracion.setText(tablamegamixes.getValueAt(filaseleccionada, 4).toString());
        cbEstilo.setSelectedItem(tablamegamixes.getValueAt(filaseleccionada, 5));
        
        
        
        
        
    }//GEN-LAST:event_tablamegamixesMouseClicked

